public class Variables_DataType {
    public static void main(String[] args) {
         int i = 1 ;
         double d=1.5;
         float f = 1.5f;
         boolean b = true;
         char c = 'a';
         String s = "Con meo";
        System.out.println("i:" +i );
        System.out.println("d:" + d);
        System.out.println("f:"+f);
        System.out.println("b:"+b);
        System.out.println("c:" + c);
        System.out.println("s:"+s);

    }
}
